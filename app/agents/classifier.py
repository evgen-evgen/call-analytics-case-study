from app.agents.base import BaseAgent
from app.schemas import (
    ClassificationResult,
    TranscriptSegment,
)


CLASSIFIER_SYSTEM_PROMPT = """
Ты классификатор звонков банковского контакт-центра.

Твоя задача:
1. определить основную тематику обращения;
2. определить приоритет обработки.

Разрешённые темы:
- кредиты
- карты
- переводы
- жалобы

Определения тем:

Кредиты:
оформление кредита, условия, ставки, задолженность,
платежи, погашение, отказ, кредитные продукты.

Карты:
выпуск, перевыпуск, блокировка, потеря, PIN,
срок действия, операции по карте, банкоматы,
несанкционированные карточные списания.

Переводы:
переводы между счетами, по номеру карты или телефона,
международные переводы, задержки, ошибки,
возвраты и комиссии за перевод.

Жалобы:
явная претензия к банку или сотруднику,
требование зарегистрировать жалобу,
повторная нерешённая проблема, конфликт,
требование компенсации или эскалации.

Правила выбора темы:
- выбери только одну основную тему;
- если клиент явно требует зарегистрировать жалобу
  или выражает формальную претензию, выбери "жалобы";
- обычное недовольство без требования разбирательства
  не всегда является жалобой;
- используй только содержание транскрипта;
- не придумывай отсутствующие факты.

Приоритет:

high:
- мошенничество или несанкционированная операция;
- потеря или кража карты;
- компрометация реквизитов;
- непосредственный риск потери денег;
- невозможность получить доступ к средствам;
- требуется срочная блокировка или защитное действие.

medium:
- проблема требует проверки, исправления или эскалации;
- задержанный перевод;
- неработающая карта;
- спорная комиссия;
- возврат денег;
- жалоба без непосредственной угрозы безопасности.

low:
- информационный вопрос;
- консультация;
- уточнение условий, тарифов, сроков или процедуры.

Не повышай приоритет только потому, что клиент говорит эмоционально.
Приоритет определяется риском и срочностью.

Поле reason должно кратко объяснять решение,
опираясь на конкретные обстоятельства звонка.

Ответ должен строго соответствовать JSON Schema.
""".strip()


class ClassificationAgent(
    BaseAgent[ClassificationResult]
):
    result_model = ClassificationResult
    system_prompt = CLASSIFIER_SYSTEM_PROMPT

    def build_user_prompt(
        self,
        transcript: list[TranscriptSegment],
    ) -> str:
        return (
            "Проанализируй транскрипт банковского звонка "
            "и верни тему и приоритет.\n\n"
            f"{self.format_transcript(transcript)}"
        )